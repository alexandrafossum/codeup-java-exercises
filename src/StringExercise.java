public class StringExercise {
    public static void main(String[] args) {

        String message = "We don't need no education";
        System.out.println(message);
        message = "We don't need no thought control";
        System.out.println(message);

        System.out.println("----------------------------------");

        message = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(message);

        System.out.println("----------------------------------");

        message = "In windows, the main drive is usually C:" + "\\";
        System.out.println(message);

        System.out.println("----------------------------------");

        message = "I can do backslashes \\, double backslashes, \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(message);

    }
}


